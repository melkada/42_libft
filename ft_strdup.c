#include "./libft.h"

char	*ft_strdup(const char *s)
{
	char	*ptr;
	size_t	i;

	if (!(ptr = (char *)malloc((sizeof(char) * (ft_strlen(s) + 1)))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = 0;
	return (ptr);
}
