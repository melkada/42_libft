#include "./libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list *elem;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		del((*lst)->content);
		elem = *lst;
		*lst = elem->next;
		free(elem);
	}
	*lst = NULL;
}
